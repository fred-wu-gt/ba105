package com.fruit.model;

public class FruitVO implements java.io.Serializable{
	private String fru_no;
	private String fru_name;
	private String fru_kno;
	private String fru_pho_base64;
	
	public String getFru_no() {
		return fru_no;
	}
	public void setFru_no(String fru_no) {
		this.fru_no = fru_no;
	}
	public String getFru_name() {
		return fru_name;
	}
	public void setFru_name(String fru_name) {
		this.fru_name = fru_name;
	}
	public String getFru_kno() {
		return fru_kno;
	}
	public void setFru_kno(String fru_kno) {
		this.fru_kno = fru_kno;
	}
	public String getFru_pho_base64() {
		return fru_pho_base64;
	}
	public void setFru_pho_base64(String fru_pho_base64) {
		this.fru_pho_base64 = fru_pho_base64;
	}
	

}
